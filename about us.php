            <h2>About the Census</h2>
            <p>The Philippine Statistics Authority (PSA) conducts periodic censuses to gather comprehensive data on the population and housing characteristics of the country. These censuses are crucial for planning and policy-making at the national and local levels.</p>
            <p>The census aims to provide accurate and up-to-date information on:</p>
            <ul>
                <li>Population size and distribution</li>
                <li>Demographic characteristics (age, sex, etc.)</li>
                <li>Household characteristics</li>
                <li>Housing conditions</li>
            </ul>
            <p>This data helps in allocating resources, designing social programs, and monitoring the country's development.</p>
